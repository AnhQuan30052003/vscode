/// Code bai hanh trinh ki tu ///

// Phan Thu vien --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
#include <iostream>
#include <fstream>
using namespace std;
// Phan Thu vien --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//


// Bien chuong trinh ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
// Du lieu
int n;
int dem = 1;
char ten[10][10];
string chuoi;
string path = "ten.txt";

// Gia toc bien doi:
int ngang[2] = {0, 1};
int doc[2]   = {1, 0};
// Bien chuong trinh ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//


// Chuong trinh con -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
// Lay ten tu file
void LayTen() {
    ifstream file(path);
    getline(file, chuoi);
    cout << "Chuoi doc duoc: " << chuoi << endl;
    n = chuoi.size();
}

// Tao mot ma tran tam giac voi du lien ten da lay
void TaoTamGiacTen() {
    for (int d = 0; d < n; d++)
        for (int c = 0; c < n-d; c++)
            ten[d][c] = chuoi[d+c]; 
}

// Hien ti ket qua
void In() {
    ten[0][0] = 'x';
    cout << "Cach: " << dem++ << endl;
    for (int d = 0; d < n; d++)
        for (int c = 0; c < n-d; c++)
            cout << ten[d][c] << (c == n-d-1 ? "\n" : " ");
    cout << endl;
}

// Tien hanh thu nghiem cac huong di 
void Thu(int x, int y, int dai) {
    for (int i = 0; i <= 1; i++) {
        x = x + ngang[i];
        y = y + doc[i];

        char ki_tu = ten[x][y];
        ten[x][y] = 'x';
        if (dai == n) In();
        else Thu(x ,y, dai+1);

        ten[x][y] = ki_tu;
        x = x - ngang[i];
        y = y - doc[i];
    }
}
// Chuong trinh con -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//


// Chuong trinh chinh ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
int main()
{
    LayTen();
    TaoTamGiacTen();
    Thu(0,0,2);
}
// Chuong trinh chinh ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//